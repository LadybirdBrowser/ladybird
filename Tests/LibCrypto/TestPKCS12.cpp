TEST_CASE(test_pkcs12_parse_basic)
{
    auto result = TRY_OR_FAIL(Crypto::Certificate::parse_pkcs12(
        { s_test_pkcs12_basic, sizeof(s_test_pkcs12_basic) },
        "testpass"sv));

    auto cert_sv = StringView { result.certificate_pem.bytes() };
    EXPECT(cert_sv.starts_with("-----BEGIN CERTIFICATE-----"sv));
    EXPECT(cert_sv.contains("-----END CERTIFICATE-----"sv));

    auto key_sv = StringView { result.private_key_pem.bytes() };
    EXPECT(key_sv.starts_with("-----BEGIN PRIVATE KEY-----"sv));
    EXPECT(key_sv.contains("-----END PRIVATE KEY-----"sv));

    EXPECT_EQ(result.ca_chain_pem.size(), 0u);
}

